<template>

<div id="redirect">
    <h1>Redirecting</h1>
</div>

</template>

<script>
import generate_query_string from "./components/generate_query_string";
export default {
    name: "redirect",
    created: function() {
        this.getData();
    },
    methods: {
        getData: function() {
            let url = {
                what: "get_user_info"
            };

            fetch("student_survey.php?" + generate_query_string(url))
                .then(res => res.json())
                .then(data => this.setData(data))
                .catch(err => {
                    this.$emit("error", err.toString());
                });
        },
        setData: function(data) {
            var override_token = "HLF43W";
            this.$router.replace({
                path: `/user_id/${
                    data.DATA.user_id
                }/override_token/${override_token}/student-landing`
            });
        }
    }
};
</script>
